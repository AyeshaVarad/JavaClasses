package Day1;

public class DataTypes {

    public static void main(String[] args){

        int students; //Variable Declaration
        students = 20; //Variable Initialization

        long aadhar = 123456789001L;  // L is mandatory tp add in long
        float weight = 45.06f; // f is mandatory tp add in float
        double fee = 2000.99;  // we can skip adding d in double


        System.out.println("Roll number of Students :"+students);
        System.out.println("Aadhar number of Students :"+aadhar);
        System.out.println("Weight of Students :"+weight);
        System.out.println("Fee of Students :"+fee);

    }

}
